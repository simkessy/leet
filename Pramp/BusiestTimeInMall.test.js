const findBusiestPeriod = require("./BusiestTimeInMall");

test("Function exist", () => {
  expect(findBusiestPeriod).toBeDefined();
});

test("Test 1", () => {
  expect(findBusiestPeriod([[1487799426, 21, 1]])).toEqual(1487799426);
});

test("Test 2", () => {
  expect(
    findBusiestPeriod([
      [1487799425, 21, 0],
      [1487799427, 22, 1],
      [1487901318, 7, 0]
    ])
  ).toEqual(1487799427);
});

